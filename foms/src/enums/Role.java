package enums;

/**
 * The Role enum represents the different roles that a user can have in the system.
 * The available roles are STAFF, BRANCHMANAGER, and ADMIN.
 */
public enum Role {
    /**
     * Represents a staff member.
     */
    STAFF,
    /**
     * Represents a branch manager.
     */
    BRANCHMANAGER,
    /**
     * Represents an admin user.
     */
    ADMIN
}
